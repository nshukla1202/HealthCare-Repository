
public class Dowhileloop {

	
	public static void main(String[] args) {

		/*
		*initialization;
		*do
		*{
		*  //block of statement;
		* //increment or decrement;
		*
		* initialization;
		*/
		//if you want to execute the loops irrespective of condition is true/false we use do while loop

		int i=0;
		i=1;
		i=2;

		do{
		  System.out.println(i);
		i++;
		} while(i<0);
}
}
